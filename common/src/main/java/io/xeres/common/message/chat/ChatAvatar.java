package io.xeres.common.message.chat;

public class ChatAvatar
{
	private byte[] image;

	public ChatAvatar()
	{
		// Needed for JSON
	}

	public ChatAvatar(byte[] image)
	{
		this.image = image;
	}

	public byte[] getImage()
	{
		return image;
	}

	public void setImage(byte[] image)
	{
		this.image = image;
	}

	@Override
	public String toString()
	{
		return "ChatAvatar{" +
				"image size=" + image.length +
				'}';
	}
}
